package ru.mirea.task3;

public class Hand {
    int fingersLeftHand;
    int fingersRightHand;

    public void setFingersLeftHand(int fingersLeftHand) {
        this.fingersLeftHand = fingersLeftHand;
    }

    public void setFingersRightHand(int fingersRightHand) {
        this.fingersRightHand = fingersRightHand;
    }

    public int getFingersLeftHand() {
        return fingersLeftHand;
    }

    public int getFingersRightHand() {
        return fingersRightHand;
    }
}
