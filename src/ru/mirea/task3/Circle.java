package ru.mirea.task3;

public class Circle {
    private int x0, y0,D, R;

    public void setD(int d) {
        D = d;
    }

    public void setR(int r) {
        R = r;
    }

    public void setX0(int x0) {
        this.x0 = x0;
    }

    public void setY0(int y0) {
        this.y0 = y0;
    }

    public int getD() {
        return D;
    }

    public int getR() {
        return R;
    }

    public int getX0() {
        return x0;
    }

    public int getY0() {
        return y0;
    }
}
