package ru.mirea.task3;

public class Head {
    int hair;
    int nose;
    int lips;
    int ears;
    int brows;

    public void setBrows(int brows) {
        this.brows = brows;
    }

    public void setEars(int ears) {
        this.ears = ears;
    }

    public void setHair(int hair) {
        this.hair = hair;
    }

    public void setLips(int lips) {
        this.lips = lips;
    }

    public void setNose(int nose) {
        this.nose = nose;
    }

    public int getBrows() {
        return brows;
    }

    public int getEars() {
        return ears;
    }

    public int getHair() {
        return hair;
    }

    public int getLips() {
        return lips;
    }

    public int getNose() {
        return nose;
    }
}
